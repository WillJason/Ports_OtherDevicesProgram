/*******************************************************************************************
	函数功能：ms的延时函数
	入口参数：ms_time:延时长度
	返回参数：无	
	函数说明：无
********************************************************************************************/
void delay_ms(uint32_t ms_time) 
{	
	uint32_t ms_z; 
	uint8_t i, j;
	for(ms_z = 0;ms_z < ms_time;ms_z ++)
	{ 
		_nop_();
		i = 11;
		j = 190;
		do
		{
			while (--j);
		} while (--i);
	}
}


